    private void jTextField_dniKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_dniKeyTyped
        // TODO add your handling code here:
        if (jTextField_dni.getText().length() >= 8) {
            String ruc = "";
            ruc = jTextField_dni.getText();
            jTextField_dni.setText(ruc.substring(0, ruc.length()));
            evt.consume();
            getToolkit().beep();
            JOptionPane.showMessageDialog(null, "Ingrese solo 8 numeros ");
        }
        char c = evt.getKeyChar();
//            String ruc=jTextField_rucKeyTyped(evt);.getText();
        if (Character.isLetter(c)) {
            evt.consume();//para q no sea procesada predetermindamente por la fuente q lo creo
            getToolkit().beep();//hacemos el sonido beep
            JOptionPane.showMessageDialog(null, "Ingrese solo numeros ");
        }

        //  Error.setText("Ingresa Solo Numeros");   
    }//GEN-LAST:event_jTextField_dniKeyTyped

