    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        //String codigo = jTextField_cod.getText();
        String nombre = jTextFieldNombre.getText();
        String apellido = jTextFieldApellido.getText();
        String dni = (jTextField_dni.getText());
        String direccion = (jTextField_direccion.getText());
        String telefono = (jTextField_telefono.getText());
        String correo = (jTextField_correo.getText());

        if (/*codigo.equals("") || */nombre.equals("") || apellido.equals("") || dni.equals("")
                || direccion.equals("") || telefono.equals("") || correo.equals("")) {

            JOptionPane.showMessageDialog(null, "Rellene Todos Los Campos");

        } else {
            Control_Cliente clidao = new Control_Cliente();
            int dni_cli = Integer.parseInt(dni);
            
            if(clidao.existeDni(dni_cli) == 0){
                add.agregarCliente(/*codigo, */nombre, apellido, dni, direccion, telefono, correo);
            }else{
                JOptionPane.showMessageDialog(rootPane, "DNI Existenete!!!");
            }
            
        }

        if (ventas == "ventas") {
            dispose();
            Ventas.main(null);
        } else {
            dispose();
            addCliente.main(null);

        }
    }//GEN-LAST:event_jButton1ActionPerformed

